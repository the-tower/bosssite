            <h3>What is BossOS?</h3>
                <p>BossOS is a arch linux based operating system themed around the corporate environment in a joking way.</p>
                <p>Mainly, BossOS is a pre-themed arch setup with some custom software included such as:</p>
                <ul>
                    <li>A phone connection script called BossPhone</li>
                    <li>A "package manager" called bopirruu</li>
                    <li>And more to come as the development of BossOS continues</li>
                </ul>
            <h3>What is bopirruu</h3>
                <p>bopirruu is the package managed developed for BossOS, it is not meant to be used in a conventional/everyday type of way.</p>
                <p>While the <a href="./bor.html">repo</a> is publicly available to download from, it does not allow unknown users to submit packages (although it may in the future.</p>
                <p>bopirruu is an acronym for:</p>
                <ul>
                    <li>Boss</li>
                    <li>OS</li>
                    <li>Package</li>
                    <li>installer</li>
                    <li>remover</li>
                    <li>reporter</li>
                    <li>updater</li>
                    <li>uploader</li>
                </ul>
        
            <h3>Why was BossOS created?</h3>
                <p>BossOS was inspired by projects such as AmogOS and joke linux distributions</p>
                <p>It was also created as a way to learn more about linux, distro designing/building, and more.</p>

            <h3>Who are the creators of BossOS?</h3>
                <p>BossOS is created by Zutki, Chicken in a can, and Minh. Our contacts can be found in the Contact section.</p>

